                        <div style="background: #00ff00; color: #000; width: 40px; height: 40px; border-radius: 50%; display: flex; align-items: center; justify-content: center; font-size: 20px; font-weight: bold; flex-shrink: 0;">4</div>
                        <h3 style="color: #00ff00; font-weight: bold; font-size: 16px; margin: 0;">Symmetric Mode</h3>
                    </div>
                    <div style="padding-left: 55px;">
                        <div class="modal-instruction-step">
                            Enable <strong>SYMSEL</strong> (Symmetric Selection) to automatically select the same tile positions on both images simultaneously.<br>
                            Useful when your LEVEL.PNG and TILESET.PNG have tiles at the same coordinates.
                        </div>
                    </div>
                </div>

                <!-- Step 5: BOUNCE Mode -->
                <div style="margin-bottom: 20px; background: #002200; border: 1px solid #00ff00; border-radius: 2px; padding: 15px;">
                    <div style="display: flex; align-items: center; gap: 15px; margin-bottom: 15px;">
                        <div style="background: #00ff00; color: #000; width: 40px; height: 40px; border-radius: 50%; display: flex; align-items: center; justify-content: center; font-size: 20px; font-weight: bold; flex-shrink: 0;">5</div>
                        <h3 style="color: #00ff00; font-weight: bold; font-size: 16px; margin: 0;">BOUNCE Mode</h3>
                    </div>
                    <div style="padding-left: 55px;">
                        <div class="modal-instruction-step">
                            <strong>BOUNCE (single swap — default)</strong><br>
                            Click the <strong>◄► BOUNCE</strong> button before adding a swap to create a <strong>ping-pong animation</strong> in one swap.<br>
                            Frames play forward then in reverse automatically:<br>
                            • Frame 1 → 2 → 3 → 2 → 1 → 2 → 3 → …<br>
                            Perfect for breathing effects, torches, water, or any looping animation.
                        </div>
                        <div class="modal-instruction-step">
                            <strong>BOUNCE ×2 (two separate swaps)</strong><br>
                            When BOUNCE is active, click the <strong>×2</strong> button to create <strong>two independent swaps</strong> instead:<br>
                            • <em>name_in</em>: plays forward (e.g., door opening)<br>
                            • <em>name_out</em>: plays in reverse (e.g., door closing)<br>
                            Use these in your GBVM scripts independently — trigger <em>door_in</em> when the player opens a door, <em>door_out</em> when it closes.<br>
                            Perfect for doors, switches, levers, and any <strong>event-driven</strong> reversible transition.
                        </div>
                    </div>
                </div>

                <!-- Step 6: Animation Modes -->
                <div style="margin-bottom: 20px; background: #002200; border: 1px solid #00ff00; border-radius: 2px; padding: 15px;">
                    <div style="display: flex; align-items: center; gap: 15px; margin-bottom: 15px;">
                        <div style="background: #00ff00; color: #000; width: 40px; height: 40px; border-radius: 50%; display: flex; align-items: center; justify-content: center; font-size: 20px; font-weight: bold; flex-shrink: 0;">6</div>
                        <h3 style="color: #00ff00; font-weight: bold; font-size: 16px; margin: 0;">Animation Modes</h3>
                    </div>
                    <div style="padding-left: 55px;">
                        <div class="modal-instruction-step">
                            The <strong>ANIMATION</strong> label in the top bar shows two mode buttons that control how multi-frame animations work for the current set:
                        </div>
                        <div class="modal-instruction-step">
                            <strong>FRAME Mode</strong><br>
                            Add multiple frame images using the <strong>+FRAME</strong> button in the top bar.<br>
                            Each frame is a separate image file. Tiles are read from each image independently.<br>
                            Remove the last frame with the <strong>✕</strong> button on its image header.
                        </div>
                        <div class="modal-instruction-step">
                            <strong>SPRITE SHEET Mode</strong><br>
                            Load a single sprite sheet with all frames packed horizontally. Use the <strong>FRAMES</strong> counter in the top bar to set the number of frames.<br>
                            <strong style="color: #ffff00;">IMPORTANT:</strong> Only select the <strong>FIRST frame</strong>. Subsequent frames are captured automatically via horizontal offset:<br>
                            • <strong>Frame 1:</strong> Your selection (e.g., column 5)<br>
                            • <strong>Frame 2:</strong> Same row, shifted right by the selection width<br>
                            • <strong>Frame 3:</strong> Shifted right again — and so on<br>
                            Arrange frames side by side horizontally in TILESET.PNG with no gaps.<br>
                            <strong>Example:</strong> A 2×2 tile character with 4 frames → lay out as an 8×2 strip, select the first 2×2 area, set FRAMES to 4.
                        </div>
                        <div class="modal-instruction-step">
                            <strong>Single Frame (default)</strong><br>
                            When set to 1 frame, no animation occurs. Tiles are captured once from your selection.
                        </div>
                    </div>
                </div>

                <!-- Step 7: Collecting Tiles (SWAPS) -->
                <div style="margin-bottom: 20px; background: #001122; border: 1px solid #04d9ff; border-radius: 2px; padding: 15px;">
