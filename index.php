                        <h3 style="color: #00ff00; font-weight: bold; font-size: 16px; margin: 0;">Create a New Set</h3>
                    </div>
                    <div style="padding-left: 55px;">
                        <div class="modal-instruction-step">
                            Click the <strong>LEVEL.PNG</strong> button (top toolbar) to load your background image — this is the scene background you use in GB Studio, showing WHERE tiles will be swapped.<br><br>
                            Click the <strong>Set 1</strong> button (or your set name) to load your second image — the source of the new tile graphics, showing WHAT will appear after the swap.<br><br>
                            The tileset name auto-fills from the filename. Avoid spaces, hyphens and underscores in filenames (see Step 9).
                        </div>
                    </div>
                </div>

                <!-- Step 2: Working with Sets -->
                <div style="margin-bottom: 20px; background: #002200; border: 1px solid #00ff00; border-radius: 2px; padding: 15px;">
                    <div style="display: flex; align-items: center; gap: 15px; margin-bottom: 15px;">
                        <div style="background: #00ff00; color: #000; width: 40px; height: 40px; border-radius: 50%; display: flex; align-items: center; justify-content: center; font-size: 20px; font-weight: bold; flex-shrink: 0;">2</div>
                        <h3 style="color: #00ff00; font-weight: bold; font-size: 16px; margin: 0;">Working with Sets</h3>
                    </div>
                    <div style="padding-left: 55px;">
                        <div class="modal-instruction-step">
                            <strong>Sets</strong> let you work with multiple pairs of images in one project.<br>
                            Use the <strong>tabs at the top</strong> of the interface to switch between sets. Click <strong>+</strong> to add a new set, and <strong>×</strong> on a tab to remove it. Double-click a tab name to rename it.
                        </div>
                        <div class="modal-instruction-step">
                            <strong>Auto-switch on hover</strong><br>
                            When you hover over a Swap in the SWAPS tab, the tool automatically switches to the set that swap was created from — the correct images are always shown.
                        </div>
                        <div class="modal-instruction-step">
                            <strong>Shared across all sets:</strong> SWAPS are global — visible from every set.<br>
                            <strong>Per-set:</strong> LEVEL.PNG, TILESET.PNG, extra frame images, and animation mode settings.
