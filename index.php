                        <div style="background: #00ff00; color: #000; width: 40px; height: 40px; border-radius: 50%; display: flex; align-items: center; justify-content: center; font-size: 20px; font-weight: bold; flex-shrink: 0;">3</div>
                        <h3 style="color: #00ff00; font-weight: bold; font-size: 16px; margin: 0;">Select Tiles</h3>
                    </div>
                    <div style="padding-left: 55px;">
                        <div class="modal-instruction-step">
                            <strong>Click</strong> individual tiles or <strong>click & drag</strong> to select rectangular areas.<br>
                            Both images must have the same number of tiles selected.
                        </div>
                    </div>
