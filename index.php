                            <strong>Add Swap</strong><br>
                            Enter a swap name (e.g., "WALK_LEFT") and click <strong>Add Swap</strong> or press <strong>Enter</strong>.<br>
                            Use <strong>Clear</strong> to reset your current selection.
                        </div>
                        <div class="modal-instruction-step">
                            <strong>Tile Usage Stats</strong><br>
                            The <strong>SWAPS</strong> heading shows live tile stats: <em>LEVEL X · N swaps Y · total Z / 192 (P%)</em>.<br>
                            GB Studio BG tilesets support up to <strong>192 tiles</strong> — watch this counter to avoid exceeding the limit!
                        </div>
                        <div class="modal-instruction-step">
                            <strong>Edit Swap Name</strong><br>
                            <strong>Double-click</strong> on any swap name to edit it. Press <strong>Enter</strong> to save or <strong>Escape</strong> to cancel.
                        </div>
                        <div class="modal-instruction-step">
                            <strong>Reorder Swaps</strong><br>
                            Click and drag the <strong>⋮⋮</strong> handle on any swap to reorder them. The tileset and tile indices update automatically.
                        </div>
                        <div class="modal-instruction-step">
                            <strong>View Code</strong><br>
                            Click the <strong>▶</strong> arrow to expand/collapse the code preview for each swap.<br>
                            Use <strong>Copy</strong> to copy the code to clipboard.<br>
                            Use <strong>Delete</strong> to remove the swap.
                        </div>
                        <div class="modal-instruction-step">
                            <strong>Code Options</strong><br>
                            Choose <strong>Type</strong> (ts/bg) and <strong>Mode</strong>:<br>
                            • <strong>TileData</strong>: For use with the <a href="https://github.com/pixelpep/gbtileswaper/releases/" target="_blank" style="color:#04d9ff;">Tile Swaper plugin</a> (recommended)<br>
                            • <strong>GBVM</strong>: Standard GBVM scripts if you don't use the plugin
                        </div>
                    </div>
                </div>

